-- =========================
-- USERS
-- =========================
CREATE TABLE users (
    user_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR2(100) NOT NULL,
    email       VARCHAR2(200) UNIQUE NOT NULL,
    password    VARCHAR2(255) NOT NULL,
    user_type   VARCHAR2(20) NOT NULL
        CHECK (LOWER(user_type) IN ('student', 'faculty')),
    department  VARCHAR2(50) NOT NULL,
    batch       NUMBER,
    joined_at   TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- =========================
-- FORMS
-- =========================
CREATE TABLE forms (
    form_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title           VARCHAR2(150) NOT NULL,
    description     CLOB,
    creator_id      NUMBER NOT NULL,
    access_type     VARCHAR2(20) NOT NULL
        CHECK (LOWER(access_type) IN ('public', 'group')),
    target_dept     VARCHAR2(50),
    deadline        TIMESTAMP NOT NULL,
    status          VARCHAR2(20) DEFAULT 'open' NOT NULL
        CHECK (LOWER(status) IN ('open', 'closed')),
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    form_code       VARCHAR2(20) NOT NULL UNIQUE,
    report_released VARCHAR2(5) DEFAULT 'no'
        CHECK (LOWER(report_released) IN ('yes', 'no')),
    theme_color     VARCHAR2(20) DEFAULT 'emerald',
    allow_multiple  VARCHAR2(5) DEFAULT 'no'
        CHECK (LOWER(allow_multiple) IN ('yes', 'no')),
    CONSTRAINT fk_forms_creator
        FOREIGN KEY (creator_id)
        REFERENCES users(user_id)
        ON DELETE CASCADE
);

-- =========================
-- AUDIT LOGS
-- =========================
CREATE TABLE audit_logs (
    log_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    action_type VARCHAR2(100) NOT NULL,
    user_id     NUMBER,
    form_id     NUMBER,
    log_time    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_audit_user
        FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE SET NULL,
    CONSTRAINT fk_audit_form
        FOREIGN KEY (form_id) REFERENCES forms(form_id) ON DELETE SET NULL
);

-- =========================
-- QUESTIONS
-- =========================
CREATE TABLE questions (
    question_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id         NUMBER NOT NULL,
    question_text   CLOB NOT NULL,
    question_type   VARCHAR2(20) NOT NULL
        CHECK (LOWER(question_type) IN ('text', 'textarea', 'number', 'email', 'date', 'mcq')),
    is_required     NUMBER(1) NOT NULL CHECK (is_required IN (0,1)),
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_questions_form
        FOREIGN KEY (form_id)
        REFERENCES forms(form_id)
        ON DELETE CASCADE
);

-- =========================
-- OPTIONS
-- =========================
CREATE TABLE options (
    option_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    question_id NUMBER NOT NULL,
    option_text CLOB NOT NULL,
    CONSTRAINT fk_options_question
        FOREIGN KEY (question_id)
        REFERENCES questions(question_id)
        ON DELETE CASCADE
);

-- =========================
-- RESPONSES
-- =========================
CREATE TABLE responses (
    response_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    form_id     NUMBER NOT NULL,
    user_id     NUMBER NOT NULL,
    submitted_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_responses_form
        FOREIGN KEY (form_id)
        REFERENCES forms(form_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_responses_user
        FOREIGN KEY (user_id)
        REFERENCES users(user_id)
        ON DELETE CASCADE
);

-- =========================
-- ANSWERS
-- =========================
CREATE TABLE answers (
    answer_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    response_id        NUMBER NOT NULL,
    question_id        NUMBER NOT NULL,
    answer_text        CLOB,
    answer_number      NUMBER(10,4),
    selected_option_id NUMBER,
    CONSTRAINT fk_answers_response
        FOREIGN KEY (response_id)
        REFERENCES responses(response_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_answers_question
        FOREIGN KEY (question_id)
        REFERENCES questions(question_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_answers_option
        FOREIGN KEY (selected_option_id)
        REFERENCES options(option_id),
    CONSTRAINT chk_one_answer_only CHECK (
        (answer_text IS NOT NULL AND answer_number IS NULL AND selected_option_id IS NULL)
        OR
        (answer_text IS NULL AND answer_number IS NOT NULL AND selected_option_id IS NULL)
        OR
        (answer_text IS NULL AND answer_number IS NULL AND selected_option_id IS NOT NULL)
    )
);

