-- 6. TRIGGER: Audit Form Creation
CREATE OR REPLACE TRIGGER trg_audit_form_creation
AFTER INSERT ON forms
FOR EACH ROW
BEGIN
    INSERT INTO audit_logs (action_type, user_id, form_id)
    VALUES ('CREATED NEW FORM', :NEW.creator_id, :NEW.form_id);
END;
/

-- 7. TRIGGER: Audit Response Submission
CREATE OR REPLACE TRIGGER trg_audit_response
AFTER INSERT ON responses
FOR EACH ROW
BEGIN
    INSERT INTO audit_logs (action_type, user_id, form_id)
    VALUES ('SUBMITTED RESPONSE', :NEW.user_id, :NEW.form_id);
END;
