-- =========================
-- PL/SQL PROCEDURES, FUNCTIONS & TRIGGERS
-- =========================

-- 1. TRIGGER: Prevent forms from being created with a past deadline
CREATE OR REPLACE TRIGGER trg_form_deadline_check
BEFORE INSERT OR UPDATE ON forms
FOR EACH ROW
BEGIN
    IF INSERTING OR (UPDATING AND :NEW.deadline != :OLD.deadline) THEN
        IF :NEW.deadline <= SYSTIMESTAMP THEN
            RAISE_APPLICATION_ERROR(-20001, 'Form deadline cannot be in the past.');
        END IF;
    END IF;
END;
/

-- 2. PROCEDURE: Automatically close expired forms
CREATE OR REPLACE PROCEDURE prc_close_expired_forms IS
BEGIN
    UPDATE forms 
    SET status = 'closed'
    WHERE status = 'open' AND deadline < SYSTIMESTAMP;
    
    COMMIT;
END;
/

-- 3. FUNCTION: Get total number of responses for a form
CREATE OR REPLACE FUNCTION fn_get_response_count(p_form_id IN NUMBER) 
RETURN NUMBER IS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM responses
    WHERE form_id = p_form_id;
    
    RETURN v_count;
END;
/

-- 4. TRIGGER: Prevent responses on closed or expired forms
CREATE OR REPLACE TRIGGER trg_prevent_invalid_response
BEFORE INSERT ON responses
FOR EACH ROW
DECLARE
    v_status VARCHAR2(20);
    v_deadline TIMESTAMP;
BEGIN
    SELECT status, deadline INTO v_status, v_deadline
    FROM forms 
    WHERE form_id = :NEW.form_id;

    IF v_status = 'closed' OR v_deadline < SYSTIMESTAMP THEN
        RAISE_APPLICATION_ERROR(-20002, 'Cannot submit response: Form is closed or expired.');
    END IF;
END;
/

-- 5. PROCEDURE: Toggle Form Status (Open/Closed)
CREATE OR REPLACE PROCEDURE prc_toggle_form_status(
    p_form_id IN NUMBER,
    p_new_status OUT VARCHAR2
) IS
    v_current_status VARCHAR2(20);
BEGIN
    SELECT status INTO v_current_status
    FROM forms 
    WHERE form_id = p_form_id;

    IF v_current_status = 'open' THEN
        p_new_status := 'closed';
    ELSE
        p_new_status := 'open';
    END IF;

    UPDATE forms 
    SET status = p_new_status 
    WHERE form_id = p_form_id;
    
    COMMIT;
END;
/

